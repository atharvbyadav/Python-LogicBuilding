#Grading marks based on scores

marks = int(input("Input Your Marks: "))

if marks >= 95:
    print("You got an A+, Congratulations!!!")
elif marks >= 90:
    print("Great Job! You got an A.")
elif marks >= 80:
    print("Good Job! You got a B.")
elif marks >= 70:
    print("You got C, you can do better.")
else:
    print("You got an F, you failed.")
